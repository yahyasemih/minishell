#include "signals.h"

void	cmd_signal_handler(int sig)
{
	if (sig == SIGINT)
	{
		printf("\n");
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
}

void	install_signal_handlers(void)
{
	rl_catch_signals = 0;
	signal(SIGINT, cmd_signal_handler);
	signal(SIGQUIT, cmd_signal_handler);
}
